package com.justmediagroup.ms.auditoriausuario.controller.dto;

import java.util.List;

import com.justmediagroup.logica_comun.patrones.command.IParam;

public class ValidarUsuarioDto implements IParam {

	private UsuarioDto usuario;

	private List<IntentosLoginDto> listadoIntentos;

	/**
	 * @return the usuario
	 */
	public UsuarioDto getUsuario() {
		return usuario;
	}

	/**
	 * @param usuario the usuario to set
	 */
	public void setUsuario(UsuarioDto usuario) {
		this.usuario = usuario;
	}

	/**
	 * @return the listadoIntentos
	 */
	public List<IntentosLoginDto> getListadoIntentos() {
		return listadoIntentos;
	}

	/**
	 * @param listadoIntentos the listadoIntentos to set
	 */
	public void setListadoIntentos(List<IntentosLoginDto> listadoIntentos) {
		this.listadoIntentos = listadoIntentos;
	}

}
